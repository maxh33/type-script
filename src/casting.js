"use strict";
// example of casting in typescript converting a variable to another type
var casting;
(function (casting) {
    let idade = 33;
    idade.toFixed;
    idade.length;
    idade.forEach(x => {
        console.log(x);
    });
    // let nome: string = 35 as string;
    let nome = 35;
    nome.length;
})(casting || (casting = {}));
